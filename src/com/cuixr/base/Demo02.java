package com.cuixr.base;

//23 数据类型详解
public class Demo02 {
    public static void main(String[] args) {
        //八大数据类型

        //整数
        int num1 = 10;  //最常用
        byte num2 = 10;
        short num3 = 10;
        long num4 = 10L;  //Long类型要在数字后面加个L

        //小数：浮点数
        float num5 = 10.1F;  //float类型要在数字后面加个F
        double num6 = 3.14159123456789;

        //字符
        char name = 'A';  //一个字
        //字符串 String不是关键字，是类
        String namea = "崔喜人";

        //布尔值
        boolean flag = true;
    }
}
